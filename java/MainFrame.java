    private void handleLockerAction(int floor, int row, int col) {
    Locker locker = lockers[floor][row][col];

     if (!locker.isOccupied()) {
        locker.setUserName(JOptionPane.showInputDialog("Enter Username for Locker#" + locker.getId()));
        String itemInput = JOptionPane.showInputDialog("Enter item name and quantity (format: name,quantity):");

        if (itemInput != null && !itemInput.trim().isEmpty()) {

            String[] itemParts = itemInput.split(",");
            if (itemParts.length == 2) {
                String itemName = itemParts[0].trim();
                itemName = itemName.toLowerCase();
                int quantity;
                try {
                    quantity = Integer.parseInt(itemParts[1].trim());
                    Items newItem; // Create a new item
                    newItem = new Items(itemName, RandomKeyGenerator.generateRandomKey(5), quantity);
                    locker.addItem(newItem); 
                    JOptionPane.showMessageDialog(this, "Item added to locker. Item id: " + newItem.getKey());
                    writeLockerInfoToFile(locker);

                } catch (NumberFormatException e) {

                    JOptionPane.showMessageDialog(this, "Invalid quantity. Please enter a valid number.");

                }

            } else {

                JOptionPane.showMessageDialog(this, "Invalid input format. Please use 'name,quantity'.");

            }

        }
    } else {
        // Show options to view locker info, add items, remove items, or clear locker
        String[] options = {"View Locker Info", "Add Items", "Remove Items", "Clear Locker"};
        int choice = JOptionPane.showOptionDialog(this, "Locker is occupied. Choose an action:",
            "Locker Options", JOptionPane.DEFAULT_OPTION, JOptionPane.INFORMATION_MESSAGE, null, options, options[0]);

        switch (choice) {
            case 0: // View Locker Info
                StringBuilder info = new StringBuilder();
                info.append("Locker ID: ").append(locker.getId()).append("\n");
                info.append("User  Name: ").append(locker.getUserName()).append("\n");
                info.append("Items in Locker:\n");
                for (Items item : locker.getItems()) {
                    if (item != null) {
                        info.append("- ").append(item.getName()).append(" (Quantity: ").append(item.getFrequency()).append(")\n");
                    }
                }
                JOptionPane.showMessageDialog(this, info.toString());
                writeLockerInfoToFile(locker);
                break;

            case 1: // Add Items
                String itemInput = JOptionPane.showInputDialog("Enter item name and quantity (format: name,quantity):");
                if (itemInput != null && !itemInput.trim().isEmpty()) {
                    String[] itemParts = itemInput.split(",");
                    if (itemParts.length == 2) {
                        String itemName = itemParts[0].trim();
                        int quantity;
                        try {
                            quantity = Integer.parseInt(itemParts[1].trim());
                            boolean itemFound = false;

                            // Check if the item already exists in the locker
                            for (Items item : locker.getItems()) {
                                if (item != null && item.getName().equalsIgnoreCase(itemName)) {
                                    item.incrementFrequency(quantity);
                                    JOptionPane.showMessageDialog(this, "Item already exists. Quantity updated. Item ID: " + item.getKey());
                                    itemFound = true;
                                    break;
                                }
                            }

                            if (!itemFound) {
                                Items newItem = new Items(itemName, RandomKeyGenerator.generateRandomKey(5), quantity);
                                locker.addItem(newItem);
                                JOptionPane.showMessageDialog(this, "Item added to locker. Item ID: " + newItem.getKey());
                                writeLockerInfoToFile(locker);
                            }

                            saveLockers();
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(this, "Invalid quantity. Please enter a valid number.");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid input format. Please use 'name,quantity'.");
                    }
                }
                break;

            case 2: // Remove Items
                String removeInput = JOptionPane.showInputDialog("Enter item key or name, and quantity to remove (format: key/name,quantity):");
                if (removeInput != null && !removeInput.trim().isEmpty()) {
                    String[] removeParts = removeInput.split(",");
                    if (removeParts.length == 2) {
                        String identifier = removeParts[0].trim();
                        int quantityToRemove;
                        try {
                            quantityToRemove = Integer.parseInt(removeParts[1].trim());
                            boolean itemFound = false;

                            for (Items item : locker.getItems()) {
                                if (item != null && (item.getKey().equalsIgnoreCase(identifier) || item.getName().equalsIgnoreCase(identifier))) {
                                    if (item.getFrequency() > quantityToRemove) {
                                        item.setFrequency(item.getFrequency() - quantityToRemove);
                                        JOptionPane.showMessageDialog(this, "Removed " + quantityToRemove + " of '" + item.getName() + "'.");
                                        writeLockerInfoToFile(locker);
                                        
                                    } else {
                                        locker.removeItem(item.getKey());
                                        JOptionPane.showMessageDialog(this, "Removed all of '" + item.getName() + "' from the locker.");
                                        writeLockerInfoToFile(locker);
                                    }
                                    itemFound = true;
                                    saveLockers();
                                    break;
                                }
                            }

                            if (!itemFound) {
                                JOptionPane.showMessageDialog(this, "Item not found in the locker.");
                            }
                        } catch (NumberFormatException e) {
                            JOptionPane.showMessageDialog(this, "Invalid quantity. Please enter a valid number.");
                        }
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid input format. Use 'key/name,quantity'.");
                    }
                }
                break;

            case 3: // Clear Locker
                locker.clearLocker();
                JOptionPane.showMessageDialog(this, "Locker cleared.");
                writeLockerInfoToFile(locker);
                break;

            default:
                break;
        }
    }
}
    private void writeLockerInfoToFile(Locker locker) {
    try (BufferedWriter writer = new BufferedWriter(new FileWriter("History.txt", true))) {
        
        writer.write("---------------------------------------------------------------"); // Separator for different locker entries
        writer.newLine();
            Date thisDate = new Date();
            SimpleDateFormat dateForm = new SimpleDateFormat("MM/dd/yyyy");
            String formattedDate = dateForm.format(thisDate);
            writer.write("Date: " + formattedDate + "\n");
        
        // Write locker ID and username
        writer.write("Locker ID: " + locker.getId());
        writer.newLine();
        writer.write("Username: " + locker.getUserName());
        writer.newLine();
        
        // Write items in the locker
        writer.write("Items in Locker:");
        writer.newLine();
        for (Items item : locker.getItems()) {
            if (item != null) {
                writer.write("key: "+ item.key);
                writer.newLine();
                writer.write("- " + item.getName() + " (Quantity: " + item.getFrequency() + ") ");
                writer.newLine();
            }
        }
        writer.write("---------------------------------------------------------------"); // Separator for different locker entries
        writer.newLine();
        writer.newLine();
        
        System.out.println("Locker info written to file successfully.");
    } catch (IOException e) {
        System.err.println("Error while writing locker info to file: " + e.getMessage());
    }
}
    
    
